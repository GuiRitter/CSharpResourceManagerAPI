test('equal', () => {
	expect(listMerge(
		['alpha'],
		['alpha'],
		equalsComparator
	)).toStrictEqual(['alpha']);
});

test('exists after', () => {
	expect(listMerge(
		['alpha', 'bravo'],
		['bravo', 'alpha'],
		equalsComparator
	)).toStrictEqual(['alpha', 'bravo']);
});

test('all', () => {
	expect(listMerge(
		['2', '8', '9', '1', '5', '3'],
		['3', '0', '6', '7', '8', '2'],
		equalsComparator
	)).toStrictEqual(['2', '8', '9', '3', '0', '1', '6', '5', '7']);
});

